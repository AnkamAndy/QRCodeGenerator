    /** Return data codeword capacity for a given version and EC level. */
    public static int dataCapacity(int version, ECLevel ec) {
        return DATA_CODEWORDS[version - 1][ec.ordinal()];
    }

    /** Return block structure info for a given version and EC level. */
    public static BlockInfo blockInfo(int version, ECLevel ec) {
        int[] d = BLOCK_DATA[version - 1][ec.ordinal()];
        return new BlockInfo(d[0], d[1], d[2], d[3], d[4], d[5]);
    }

    /** Find the smallest version that fits byteCount data bytes. */
    public static int minVersion(int byteCount, ECLevel ec) {
        for (int v = 1; v <= 40; v++)
            if (dataCapacity(v, ec) >= byteCount) return v;
        throw new IllegalArgumentException("Data too large for QR code: " + byteCount + " bytes");
    }

